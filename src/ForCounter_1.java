public class ForCounter_1 {
    public static void main(String[] args) {

        //Start a for loop
        for (int x = 0; x < 10; x++) {
            System.out.printf("%d ", x);
        }

        System.out.println();

        for (int x = 0; x < 10; x++) {
            System.out.printf("%d ", x + 1);
        }
    }
}
